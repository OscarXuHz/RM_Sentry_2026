// ===== CAN ID 定义 =====
#define CAN_ID_NAVIGATION  0x113    // NUC → C板：导航数据
#define CAN_ID_MOTION      0x114    // NUC → C板：姿态命令（预留）
#define CAN_ID_MCU_DATA    0x411    // C板 → NUC：yaw角度、IMU等数据

// ===== C板 → NUC 数据帧 (CAN ID: 0x411, 12字节) =====
// 数据格式: sof(1) + yaw_angle(4) + chassis_imu(4) + crc8(1) + padding(2)
// 注意: CRC8 计算范围为字节0-8（sof 到 chassis_imu）
struct CANDataFrameFromMCU
{
    uint8_t  sof;            // 字节0: 帧头 0x91
    float    yaw_angle;      // 字节1-4: 云台yaw角度 (弧度, 世界坐标系)
    float    chassis_imu;    // 字节5-8: 底盘IMU角度 (弧度, 世界坐标系)
                             //          = yaw_imu_angle - yaw_relative_ecd_angle
    uint8_t  crc8;           // 字节9: CRC8校验 (计算字节0-8)
    uint8_t  padding[2];     // 字节10-11: 填充字节 (CAN FD对齐)
} __attribute__((packed));

static_assert(sizeof(CANDataFrameFromMCU) == 12, "CANDataFrameFromMCU must be exactly 12 bytes");

// ===== NUC → C板 导航数据帧 (CAN ID: 0x113, 8字节) =====
// 协议文档: NUC 通讯协议 (CAN3 版本)
// 数据格式: vx(2) + vy(2) + z_angle(2) + flags(1) + reserved(1) = 8字节
// 
// 编码规则 (M)：
//   vx (m/s) × 1000.0 → int16 (mm/s, 原始值范围: -32768~32767)
//   vy (m/s) × 1000.0 → int16 (mm/s, 原始值范围: -32768~32767)
//   z_angle (rad) × 1000.0 → int16 (mrad, 原始值范围: -32768~32767)
//   flags: bit0 = arrived (是否到达目标点)
//   reserved = 0
struct CANNavigationFrame
{
    int16_t  vx;             // 字节0-1: X方向速度 (int16, 编码值 = vx * 1000)
    int16_t  vy;             // 字节2-3: Y方向速度 (int16, 编码值 = vy * 1000)
    int16_t  z_angle;        // 字节4-5: 目标偏航角 (int16, 编码值 = z_angle * 1000)
    uint8_t  flags;          // 字节6: 标志位 (bit0: arrived = 1表示已到达)
    uint8_t  reserved;       // 字节7: 保留字节 = 0
} __attribute__((packed));

static_assert(sizeof(CANNavigationFrame) == 8, "CANNavigationFrame must be exactly 8 bytes");

// ===== NUC → C板 姿态命令帧 (CAN ID: 0x114, 5字节) =====
// 协议文档: NUC 通讯协议 (CAN3 版本)
// 数据格式: motion_mode(1) + hp_up(1) + bullet_up(1) + bullet_num(1) + reserved(1) = 5字节
struct CANMotionCommandFrame
{
    uint8_t  motion_mode;    // 字节0: 运动模式 (0=进攻, 1=防御, 2=移动, 3=制动)
    uint8_t  hp_up;          // 字节1: 回血标志 (0=不回血, 1=回血)
    uint8_t  bullet_up;      // 字节2: 买弹标志 (0=不买弹, 1=买弹)
    uint8_t  bullet_num;     // 字节3: 买弹数量 (0-255)
    uint8_t  reserved;       // 字节4: 保留字节 = 0
} __attribute__((packed));

static_assert(sizeof(CANMotionCommandFrame) == 5, "CANMotionCommandFrame must be exactly 5 bytes");

